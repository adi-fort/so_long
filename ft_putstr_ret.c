#include "ft_printf.h"

int	ft_putstr_ret(char *str)
{
	int	i;

	i = -1;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[++i])
	{
		(write(1, &str[i], 1));
	}
	return (i);
}
